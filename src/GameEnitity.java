public class GameEnitity {

    private int health;
    private int damage;
    private String  superResilience;

    public int getHealth() {
        return health;
    }

    public void setHealth(int health) {
        this.health = health;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public String getSuperResilience() {
        return superResilience;
    }

    public void setSuperResilience(String superResilience) {
        this.superResilience = superResilience;
    }
}
